const languageOrder = ["fr", "en", "pt", "he"];
let currentLang = 'fr';
export function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
}
export function getLanguage() {
    return currentLang;
}
export function initLanguage() {
    const saved = localStorage.getItem('lang');
    if (saved && translations[saved]) {
        currentLang = saved;
    }
}
export function nextLanguage() {
    const currentIndex = languageOrder.indexOf(currentLang);
    const nextIndex = (currentIndex + 1) % languageOrder.length;
    return languageOrder[nextIndex];
}
export function t(key) {
    return translations[currentLang][key] || key;
}
export function applyTranslations(root = document) {
    root.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.dataset.i18n;
        if (!key)
            return;
        element.textContent = t(key);
    });
    root.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
        const key = element.dataset.i18nPlaceholder;
        if (!key)
            return;
        element.placeholder = t(key);
    });
    root.querySelectorAll("[data-i18n-alt]").forEach((element) => {
        const key = element.dataset.i18nAlt;
        if (!key)
            return;
        element.setAttribute("alt", t(key));
    });
    const documentTitle = document.querySelector("title[data-i18n]");
    if (documentTitle?.dataset.i18n) {
        document.title = t(documentTitle.dataset.i18n);
    }
    document.documentElement.lang = currentLang;
    document.documentElement.removeAttribute("dir");
}
